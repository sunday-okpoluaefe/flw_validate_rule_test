/**
 * Booking Schema
 */
const mongoose = require('mongoose');

module.exports.BookingSchema = new mongoose.Schema({
    account : { type: mongoose.Schema.Types.ObjectId, index: true, ref: "Account"},
    trip: { type: mongoose.Schema.Types.ObjectId, index: true, ref: "Trip"},
    location: {
        value: { type: String },
        address: { type: String },
    },
    destination: {
        value: { type: String },
        address: { type: String },
    },
    seat: { type: Number, default: 0 },
    status: { type: String, enum: ['accepted', 'declined', 'pending'], default: 'pending'}
}, { timestamps: true })


